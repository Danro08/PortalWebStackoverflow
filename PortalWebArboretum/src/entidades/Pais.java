package entidades;

public class Pais {
	
	//Atributos
	private int estado;
	private String nombre;
	private int paisID;
	
	//Metodos
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getPaisID() {
		return paisID;
	}
	public void setPaisID(int paisID) {
		this.paisID = paisID;
	}
}

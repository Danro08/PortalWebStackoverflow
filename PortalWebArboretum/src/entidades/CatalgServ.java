package entidades;

public class CatalgServ {
	private int idCatalgoServ;
	private String nombre;
	private String tipo;
	private int estado;
	
	//Metodos
	public int getIdCatalgoServ() {
		return idCatalgoServ;
	}
	public void setIdCatalgoServ(int idCatalgoServ) {
		this.idCatalgoServ = idCatalgoServ;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	

}

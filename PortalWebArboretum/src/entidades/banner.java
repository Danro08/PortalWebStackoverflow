package entidades;

public class banner {
	private String img;
	private int tbl_bannerID;
	private int estado;
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getTbl_bannerID() {
		return tbl_bannerID;
	}
	public void setTbl_bannerID(int tbl_bannerID) {
		this.tbl_bannerID = tbl_bannerID;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
}

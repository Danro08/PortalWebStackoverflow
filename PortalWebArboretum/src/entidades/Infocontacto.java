package entidades;

public class Infocontacto {
	
	//atributos
	private String correo;
	private String descripcion;
	private String telefono;
	private int tbl_infocontactoID;
	private int estado;
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public int getTbl_infocontactoID() {
		return tbl_infocontactoID;
	}
	public void setTbl_infocontactoID(int tbl_infocontactoID) {
		this.tbl_infocontactoID = tbl_infocontactoID;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	

}
